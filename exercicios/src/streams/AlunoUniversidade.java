package streams;

public class AlunoUniversidade {

	String nome;
	String curso;
	int semestre;
	boolean fezRematricula;
	
	public AlunoUniversidade(String nome, String curso, int semestre, boolean fezRematricula) {
		this.nome = nome;
		this.curso = curso;
		this.semestre = semestre;
		this.fezRematricula = fezRematricula;
	}
	
	
}
